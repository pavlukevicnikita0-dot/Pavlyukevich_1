#include <stdio.h>
#include <stdlib.h>

int main() {
    FILE *файл;
    int *массив;
    int размер;
    
    // Создаём массив
    printf("Сколько чисел будет в массиве? ");
    scanf("%d", &размер);
    
    массив = (int*)malloc(размер * sizeof(int));
    if (массив == NULL) {
        printf("Ошибка выделения памяти!\n");
        return 1;
    }
    
    // Заполняем массив
    printf("Введи %d чисел:\n", размер);
    for (int i = 0; i < размер; i++) {
        printf("arr[%d]: ", i);
        scanf("%d", &массив[i]);
    }
    
    // ===== ЗАПИСЬ В БИНАРНЫЙ ФАЙЛ =====
    printf("\n=== ЗАПИСЬ В БИНАРНЫЙ ФАЙЛ ===\n");
    
    файл = fopen("data.bin", "wb");  // "wb" - write binary
    if (файл == NULL) {
        printf("Ошибка: не могу создать бинарный файл!\n");
        free(массив);
        return 1;
    }
    
    // fwrite(что_писать, размер_одного_элемента, сколько_элементов, куда_писать)
    size_t записано = fwrite(массив, sizeof(int), размер, файл);
    
    printf("Записано %zu элементов из %d\n", записано, размер);
    
    fclose(файл);
    printf("Файл 'data.bin' создан\n");
    
    // ===== ЧТЕНИЕ ИЗ БИНАРНОГО ФАЙЛА =====
    printf("\n=== ЧТЕНИЕ ИЗ БИНАРНОГО ФАЙЛА ===\n");
    
    // Сначала узнаем размер файла
    файл = fopen("data.bin", "rb");  // "rb" - read binary
    if (файл == NULL) {
        printf("Ошибка: не могу открыть файл!\n");
        free(массив);
        return 1;
    }
    
    // Узнаём размер файла в байтах
    fseek(файл, 0, SEEK_END);  // идём в конец файла
    long размер_файла = ftell(файл);  // узнаём позицию (размер)
    rewind(файл);  // возвращаемся в начало
    
    int количество_элементов = размер_файла / sizeof(int);
    printf("В файле %d элементов\n", количество_элементов);
    
    // Выделяем память под прочитанные данные
    int *прочитанный = (int*)malloc(размер_файла);
    if (прочитанный == NULL) {
        printf("Ошибка выделения памяти!\n");
        fclose(файл);
        free(массив);
        return 1;
    }
    
    // Читаем
    size_t прочитано = fread(прочитанный, sizeof(int), количество_элементов, файл);
    printf("Прочитано %zu элементов\n", прочитано);
    
    fclose(файл);
    
    // Выводим данные
    printf("\nПрочитанный массив:\n");
    for (int i = 0; i < прочитано; i++) {
        printf("%d ", прочитанный[i]);
    }
    printf("\n");
    
    // Освобождаем память
    free(массив);
    free(прочитанный);
    
    return 0;
}