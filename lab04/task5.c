#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main() {
    FILE *файл;
    char строка[500];
    int номер_строки = 1;
    
    printf("=== ЗАПИСЬ ТЕКСТА В ФАЙЛ ===\n");
    printf("Вводи текст (пустая строка - закончить):\n\n");
    
    // Открываем файл для записи
    файл = fopen("mytext.txt", "w");
    if (файл == NULL) {
        printf("Ошибка: не могу создать файл!\n");
        return 1;
    }
    
    // Вводим строки, пока не введут пустую
    while (1) {
        printf("%d: ", номер_строки);
        fgets(строка, sizeof(строка), stdin);
        
        // Убираем символ новой строки для проверки
        строка[strcspn(строка, "\n")] = 0;
        
        // Если ввели пустую строку - выходим
        if (strlen(строка) == 0) {
            break;
        }
        
        // Добавляем перевод строки обратно и пишем в файл
        strcat(строка, "\n");
        fputs(строка, файл);
        
        номер_строки++;
    }
    
    fclose(файл);
    printf("\n✓ Текст сохранен в файл 'mytext.txt'\n\n");
    
    // Читаем и выводим содержимое файла
    printf("=== СОДЕРЖИМОЕ ФАЙЛА ===\n");
    
    файл = fopen("mytext.txt", "r");
    if (файл == NULL) {
        printf("Ошибка: не могу открыть файл для чтения!\n");
        return 1;
    }
    
    номер_строки = 1;
    while (fgets(строка, sizeof(строка), файл) != NULL) {
        printf("%d: %s", номер_строки, строка);
        номер_строки++;
    }
    
    fclose(файл);
    
    return 0;
}