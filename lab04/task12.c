#include <stdio.h>
#include <stdlib.h>
#include <string.h>

// Структура Person
struct Person {
    char имя[50];
    int возраст;
};

// Функция ввода одного человека
void ввести_человека(struct Person *человек, int номер) {
    printf("\n--- Человек %d ---\n", номер);
    
    printf("  Имя: ");
    fgets(человек->имя, sizeof(человек->имя), stdin);
    человек->имя[strcspn(человек->имя, "\n")] = 0;  // убираем \n
    
    printf("  Возраст: ");
    scanf("%d", &человек->возраст);
    getchar();  // чистим буфер
}

// Функция записи массива в файл
int записать_в_файл(const char *имя_файла, struct Person *люди, int количество) {
    FILE *файл = fopen(имя_файла, "wb");
    
    // Проверка открытия файла
    if (файл == NULL) {
        printf("\n✗ ОШИБКА: Не могу открыть файл '%s' для записи!\n", имя_файла);
        printf("  Проверьте права доступа и свободное место.\n");
        return 0;
    }
    
    // Записываем количество записей сначала (чтобы знать, сколько читать)
    fwrite(&количество, sizeof(int), 1, файл);
    
    // Записываем массив структур
    size_t записано = fwrite(люди, sizeof(struct Person), количество, файл);
    
    // Закрываем файл
    fclose(файл);
    
    if (записано == количество) {
        printf("\n✓ Успешно записано %d человек в файл '%s'\n", количество, имя_файла);
        return 1;
    } else {
        printf("\n✗ Ошибка: Записано только %zu из %d записей\n", записано, количество);
        return 0;
    }
}

// Функция чтения из файла
struct Person* прочитать_из_файла(const char *имя_файла, int *количество) {
    FILE *файл = fopen(имя_файла, "rb");
    
    // Проверка открытия файла
    if (файл == NULL) {
        printf("\n✗ ОШИБКА: Не могу открыть файл '%s' для чтения!\n", имя_файла);
        printf("  Возможно, файл ещё не создан.\n");
        return NULL;
    }
    
    // Читаем количество записей
    if (fread(количество, sizeof(int), 1, файл) != 1) {
        printf("\n✗ ОШИБКА: Файл повреждён или пуст\n");
        fclose(файл);
        return NULL;
    }
    
    if (*количество <= 0) {
        printf("\nВ файле нет записей\n");
        fclose(файл);
        return NULL;
    }
    
    // Выделяем память под массив структур
    struct Person *люди = (struct Person*)malloc(*количество * sizeof(struct Person));
    if (люди == NULL) {
        printf("\n✗ ОШИБКА: Не хватает памяти!\n");
        fclose(файл);
        return NULL;
    }
    
    // Читаем массив структур
    size_t прочитано = fread(люди, sizeof(struct Person), *количество, файл);
    
    // Закрываем файл
    fclose(файл);
    
    if (прочитано != *количество) {
        printf("\n✗ ОШИБКА: Прочитано только %zu из %d записей\n", прочитано, *количество);
        free(люди);
        return NULL;
    }
    
    printf("\n✓ Успешно прочитано %d записей из файла '%s'\n", *количество, имя_файла);
    return люди;
}

// Функция вывода всех людей
void вывести_всех(struct Person *люди, int количество) {
    if (количество == 0) {
        printf("\nСписок пуст\n");
        return;
    }
    
    printf("\n╔════════════════════════════════════════╗\n");
    printf("║           СПИСОК ЛЮДЕЙ                ║\n");
    printf("╠════════════════════════════════════════╣\n");
    
    for (int i = 0; i < количество; i++) {
        printf("║ %2d. %-30s %3d лет ║\n", 
               i + 1, 
               люди[i].имя, 
               люди[i].возраст);
    }
    
    printf("╚════════════════════════════════════════╝\n");
    printf("  Всего: %d человек(а)\n", количество);
}

// Функция добавления нового человека (перезапись)
void добавить_человека(const char *имя_файла) {
    struct Person новый;
    int текущее_количество;
    struct Person *все;
    
    // Пробуем прочитать существующие данные
    все = прочитать_из_файла(имя_файла, &текущее_количество);
    
    // Создаём новый массив
    int новое_количество = текущее_количество + 1;
    struct Person *новый_массив = (struct Person*)malloc(новое_количество * sizeof(struct Person));
    
    if (новый_массив == NULL) {
        printf("\n✗ Ошибка памяти!\n");
        if (все) free(все);
        return;
    }
    
    // Копируем старые данные
    for (int i = 0; i < текущее_количество; i++) {
        strcpy(новый_массив[i].имя, все[i].имя);
        новый_массив[i].возраст = все[i].возраст;
    }
    
    // Вводим нового человека
    printf("\n=== ДОБАВЛЕНИЕ НОВОГО ЧЕЛОВЕКА ===\n");
    ввести_человека(&новый, новое_количество);
    
    // Добавляем в конец
    strcpy(новый_массив[текущее_количество].имя, новый.имя);
    новый_массив[текущее_количество].возраст = новый.возраст;
    
    // Сохраняем обратно
    записать_в_файл(имя_файла, новый_массив, новое_количество);
    
    // Чистим память
    if (все) free(все);
    free(новый_массив);
}

// Функция удаления человека
void удалить_человека(const char *имя_файла) {
    struct Person *все;
    int количество;
    int номер;
    
    // Читаем всех
    все = прочитать_из_файла(имя_файла, &количество);
    if (все == NULL || количество == 0) {
        printf("\nНет данных для удаления\n");
        return;
    }
    
    // Показываем список
    вывести_всех(все, количество);
    
    printf("\nВведите номер человека для удаления (1-%d): ", количество);
    scanf("%d", &номер);
    getchar();
    
    if (номер < 1 || номер > количество) {
        printf("\n✗ Неверный номер!\n");
        free(все);
        return;
    }
    
    // Создаём новый массив без удаляемого
    int новое_количество = количество - 1;
    struct Person *новый_массив = (struct Person*)malloc(новое_количество * sizeof(struct Person));
    
    if (новый_массив == NULL) {
        printf("\n✗ Ошибка памяти!\n");
        free(все);
        return;
    }
    
    // Копируем всё, кроме удаляемого
    int j = 0;
    for (int i = 0; i < количество; i++) {
        if (i != номер - 1) {
            strcpy(новый_массив[j].имя, все[i].имя);
            новый_массив[j].возраст = все[i].возраст;
            j++;
        }
    }
    
    // Сохраняем
    записать_в_файл(имя_файла, новый_массив, новое_количество);
    
    printf("\n✓ Человек удалён!\n");
    
    free(все);
    free(новый_массив);
}

// Функция поиска по имени
void найти_по_имени(const char *имя_файла) {
    struct Person *все;
    int количество;
    char поиск[50];
    int найдено = 0;
    
    все = прочитать_из_файла(имя_файла, &количество);
    if (все == NULL || количество == 0) {
        printf("\nНет данных для поиска\n");
        return;
    }
    
    printf("\nВведите имя для поиска: ");
    fgets(поиск, sizeof(поиск), stdin);
    поиск[strcspn(поиск, "\n")] = 0;
    
    printf("\n=== РЕЗУЛЬТАТЫ ПОИСКА ===\n");
    for (int i = 0; i < количество; i++) {
        if (strstr(все[i].имя, поиск) != NULL) {
            printf("  %d. %s, %d лет\n", i + 1, все[i].имя, все[i].возраст);
            найдено = 1;
        }
    }
    
    if (!найдено) {
        printf("  Ничего не найдено\n");
    }
    
    free(все);
}

