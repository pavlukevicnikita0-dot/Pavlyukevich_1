#include <stdio.h>
#include <stdlib.h>

// Функция возвращает размер файла или -1 при ошибке
long получить_размер_файла(const char *имя_файла) {
    FILE *файл = fopen(имя_файла, "rb");
    if (файл == NULL) {
        return -1;  // Ошибка - файл не открылся
    }
    
    // Перемещаемся в конец
    fseek(файл, 0, SEEK_END);
    
    // Узнаём позицию
    long размер = ftell(файл);
    
    fclose(файл);
    return размер;
}

int main() {
    char имя_файла[200];
    long размер;
    
    printf("=== ОПРЕДЕЛИТЕЛЬ РАЗМЕРА ФАЙЛА ===\n");
    printf("Введи имя файла: ");
    scanf("%s", имя_файла);
    
    размер = получить_размер_файла(имя_файла);
    
    if (размер == -1) {
        printf("Ошибка: не могу открыть файл '%s'\n", имя_файла);
        return 1;
    }
    
    printf("\nФайл: %s\n", имя_файла);
    printf("Размер: %ld байт\n", размер);
    
    // Выводим в удобных единицах
    if (размер < 1024) {
        printf("Это %ld байт\n", размер);
    } else if (размер < 1024 * 1024) {
        printf("Это %.2f КБ (килобайт)\n", размер / 1024.0);
    } else if (размер < 1024 * 1024 * 1024) {
        printf("Это %.2f МБ (мегабайт)\n", размер / (1024.0 * 1024.0));
    } else {
        printf("Это %.2f ГБ (гигабайт)\n", размер / (1024.0 * 1024.0 * 1024.0));
    }
    
    return 0;
}