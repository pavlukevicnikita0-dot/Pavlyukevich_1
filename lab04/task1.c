#include <stdio.h>

int main() {
    FILE *файл;  // указатель на файл
    
    // 1. Открываем файл "test.txt" в режиме записи ("w" - write)
    файл = fopen("test.txt", "w");
    
    // 2. Проверяем, что файл успешно открылся
    if (файл == NULL) {
        // 3. Выводим сообщение об ошибке
        printf("Ошибка! Не удалось открыть файл test.txt\n");
        return 1;  // Выходим с кодом ошибки
    }
    
    printf("Файл test.txt успешно открыт для записи!\n");
    
    // 4. Закрываем файл
    fclose(файл);
    printf("Файл закрыт\n");
    
    return 0;
}