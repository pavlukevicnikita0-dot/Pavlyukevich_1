#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main() {
    FILE *файл;
    char строка[500];
    int номер_строки = 1;
    
    // Записываем строки, введенные пользователем
    printf("=== ВВОД ТЕКСТА ===\n");
    printf("Вводи строки (пустая строка - закончить):\n\n");
    
    файл = fopen("test.txt", "w");
    if (файл == NULL) {
        printf("Ошибка!\n");
        return 1;
    }
    
    while (1) {
        printf("Строка %d: ", номер_строки);
        fgets(строка, sizeof(строка), stdin);
        
        // Убираем символ новой строки
        строка[strcspn(строка, "\n")] = 0;
        
        // Если ввели пустую строку - выходим
        if (strlen(строка) == 0) {
            break;
        }
        
        // Добавляем символ новой строки обратно и пишем в файл
        strcat(строка, "\n");
        fputs(строка, файл);
        
        номер_строки++;
    }
    
    fclose(файл);
    printf("\nТекст сохранен!\n\n");
    
    // Читаем и выводим
    printf("=== СОХРАНЕННЫЙ ТЕКСТ ===\n");
    
    файл = fopen("test.txt", "r");
    if (файл == NULL) {
        printf("Ошибка!\n");
        return 1;
    }
    
    номер_строки = 1;
    while (fgets(строка, sizeof(строка), файл) != NULL) {
        printf("%d: %s", номер_строки, строка);
        номер_строки++;
    }
    
    fclose(файл);
    
    return 0;
}