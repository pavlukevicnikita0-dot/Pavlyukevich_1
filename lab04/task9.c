#include <stdio.h>
#include <stdlib.h>

int main() {
    FILE *файл;
    char буфер[100];
    
    // Сначала создадим файл с текстом
    файл = fopen("test.txt", "w");
    if (файл) {
        fprintf(файл, "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ");
        fclose(файл);
    }
    
    // Открываем для чтения
    файл = fopen("test.txt", "r");
    if (файл == NULL) {
        printf("Ошибка открытия файла!\n");
        return 1;
    }
    
    printf("Содержимое файла: 0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ\n\n");
    
    // ===== ПЕРЕМЕЩАЕМСЯ С ПОМОЩЬЮ fseek =====
    
    // 1. Перемещаемся на 5 символов от начала
    fseek(файл, 5, SEEK_SET);
    fread(буфер, 1, 5, файл);
    буфер[5] = '\0';
    printf("Позиция 5 от начала: '%s'\n", буфер);
    
    // 2. Перемещаемся на 10 символов от текущей позиции
    fseek(файл, 10, SEEK_CUR);
    fread(буфер, 1, 5, файл);
    буфер[5] = '\0';
    printf("Ещё +10 от текущей: '%s'\n", буфер);
    
    // 3. Перемещаемся на 5 символов от конца
    fseek(файл, -5, SEEK_END);
    fread(буфер, 1, 5, файл);
    буфер[5] = '\0';
    printf("За 5 символов от конца: '%s'\n", буфер);
    
    // 4. Перемещаемся в начало
    fseek(файл, 0, SEEK_SET);
    fread(буфер, 1, 10, файл);
    буфер[10] = '\0';
    printf("Снова начало (10 символов): '%s'\n", буфер);
    
    fclose(файл);
    
    return 0;
}