#include <stdio.h>
#include <stdlib.h>

int *массив = NULL;  // наш динамический массив
int размер = 0;      // текущий размер

// Прототипы функций
void добавить_элемент();
void удалить_элемент();
void вывести_список();
void изменить_размер();
void выход();

// Структура для пункта меню
struct MenuItem {
    char *название;
    void (*функция)();
};

// ===== РЕАЛИЗАЦИЯ ФУНКЦИЙ =====

void добавить_элемент() {
    int значение, позиция;
    
    printf("\n--- ДОБАВЛЕНИЕ ЭЛЕМЕНТА ---\n");
    printf("Какое число добавить? ");
    scanf("%d", &значение);
    
    printf("Куда добавить?\n");
    printf("  0 - в начало\n");
    printf("  %d - в конец\n", размер);
    printf("  любое другое число - на позицию: ");
    scanf("%d", &позиция);
    
    // Проверка позиции
    if (позиция < 0) позиция = 0;
    if (позиция > размер) позиция = размер;
    
    // Увеличиваем массив
    размер++;
    массив = (int*)realloc(массив, размер * sizeof(int));
    
    // Сдвигаем элементы вправо
    for (int i = размер - 1; i > позиция; i--) {
        массив[i] = массив[i - 1];
    }
    
    // Вставляем новый элемент
    массив[позиция] = значение;
    
    printf("✓ Элемент %d добавлен на позицию %d\n", значение, позиция);
    вывести_список();
}

void удалить_элемент() {
    if (размер == 0) {
        printf("\n✗ Массив пуст, нечего удалять!\n");
        return;
    }
    
    int позиция;
    
    printf("\n--- УДАЛЕНИЕ ЭЛЕМЕНТА ---\n");
    вывести_список();
    printf("\nКакой элемент удалить? (индекс от 0 до %d): ", размер - 1);
    scanf("%d", &позиция);
    
    if (позиция < 0 || позиция >= размер) {
        printf("✗ Неверный индекс!\n");
        return;
    }
    
    int удаленное = массив[позиция];
    
    // Сдвигаем элементы влево
    for (int i = позиция; i < размер - 1; i++) {
        массив[i] = массив[i + 1];
    }
    
    // Уменьшаем массив
    размер--;
    массив = (int*)realloc(массив, размер * sizeof(int));
    
    printf("✓ Элемент %d удален\n", удаленное);
    
    if (размер > 0) {
        вывести_список();
    } else {
        printf("Массив теперь пуст\n");
    }
}

void вывести_список() {
    printf("\n--- ТЕКУЩИЙ СПИСОК ---\n");
    if (размер == 0) {
        printf("Список пуст\n");
        return;
    }
    
    printf("Размер: %d элемент", размер);
    if (размер % 10 == 1 && размер != 11) printf("\n");
    else printf("ов\n");
    
    printf("[");
    for (int i = 0; i < размер; i++) {
        printf("%d", массив[i]);
        if (i < размер - 1) printf(", ");
    }
    printf("]\n");
    
    // Для наглядности с индексами
    printf("Индексы: ");
    for (int i = 0; i < размер; i++) {
        printf("%d", i);
        if (i < размер - 1) printf("  ");
    }
    printf("\n");
}

void изменить_размер() {
    int новый_размер;
    
    printf("\n--- ИЗМЕНЕНИЕ РАЗМЕРА ---\n");
    printf("Текущий размер: %d\n", размер);
    printf("Новый размер: ");
    scanf("%d", &новый_размер);
    
    if (новый_размер < 0) {
        printf("✗ Размер не может быть отрицательным!\n");
        return;
    }
    
    int старый_размер = размер;
    размер = новый_размер;
    
    // Перевыделяем память
    массив = (int*)realloc(массив, размер * sizeof(int));
    
    if (размер > старый_размер) {
        // Если увеличили, обнуляем новые элементы
        for (int i = старый_размер; i < размер; i++) {
            массив[i] = 0;
        }
        printf("✓ Массив увеличен до %d элементов (новые элементы = 0)\n", размер);
    } else {
        printf("✓ Массив уменьшен до %d элементов\n", размер);
    }
    
    if (размер > 0) {
        вывести_список();
    } else {
        printf("Массив теперь пуст\n");
    }
}

void выход() {
    printf("\nДо свидания! Освобождаем память...\n");
    if (массив != NULL) {
        free(массив);
    }
    exit(0);
}

// ===== ГЛАВНАЯ ФУНКЦИЯ =====

int main() {
    // Массив указателей на функции (вот он, хитрый способ без if/else)
    struct MenuItem меню[] = {
        {"1. Добавить элемент", добавить_элемент},
        {"2. Удалить элемент", удалить_элемент},
        {"3. Вывести список", вывести_список},
        {"4. Изменить размер", изменить_размер},
        {"0. Выход", выход}
    };
    
    int количество_пунктов = sizeof(меню) / sizeof(меню[0]);
    int выбор;
    
    printf("=====================================\n");
    printf("   ДИНАМИЧЕСКИЙ СПИСОК ЧИСЕЛ v1.0   \n");
    printf("=====================================\n");
    
    while (1) {
        // Выводим меню
        printf("\n┌─────────────────────────────────┐\n");
        printf("│            М Е Н Ю               │\n");
        printf("├─────────────────────────────────┤\n");
        for (int i = 0; i < количество_пунктов; i++) {
            printf("│  %-29s │\n", меню[i].название);
        }
        printf("└─────────────────────────────────┘\n");
        printf("Ваш выбор: ");
        
        scanf("%d", &выбор);
        
        // Магия: вызываем функцию по номеру без if/else и switch
        // Ищем нужный пункт меню
        int найден = 0;
        for (int i = 0; i < количество_пунктов; i++) {
            // Сравниваем первую цифру в названии пункта с выбором
            char первый_символ = меню[i].название[0];
            if (первый_символ - '0' == выбор) {
                меню[i].функция();  // Вызываем функцию!
                найден = 1;
                break;
            }
        }
        
        if (!найден && выбор != 0) {
            printf("\n✗ Неверный выбор! Попробуйте снова.\n");
        }
        
        if (выбор == 0) {
            break;  // Выход уже обработан в функции выход()
        }
    }
    
    return 0;
}