#include <stdio.h>
#include <stdlib.h>

// Функция для вставки элемента по индексу
int вставить_элемент(int **arr, int *размер, int индекс, int значение) {
    // Проверяем, корректный ли индекс (можно вставить и в конец)
    if (индекс < 0 || индекс > *размер) {
        printf("Ошибка: неверный индекс! (можно от 0 до %d)\n", *размер);
        return 0;
    }
    
    // Увеличиваем размер на 1
    (*размер)++;
    
    // Перевыделяем память под новый размер
    int *temp = (int*)realloc(*arr, (*размер) * sizeof(int));
    if (temp == NULL) {
        printf("Ошибка: не удалось расширить память!\n");
        (*размер)--; // Возвращаем размер обратно
        return 0;
    }
    *arr = temp;
    
    // Сдвигаем элементы вправо начиная с конца
    for (int i = *размер - 1; i > индекс; i--) {
        (*arr)[i] = (*arr)[i - 1];
    }
    
    // Вставляем новый элемент
    (*arr)[индекс] = значение;
    
    return 1;
}

int main() {
    int *arr;
    int n, индекс, значение;
    
    // Создаём массив
    printf("Сколько элементов будет в массиве? ");
    scanf("%d", &n);
    
    if (n <= 0) {
        printf("Размер должен быть положительным!\n");
        return 1;
    }
    
    // Выделяем память
    arr = (int*)malloc(n * sizeof(int));
    if (arr == NULL) {
        printf("Память не выделилась!\n");
        return 1;
    }
    
    // Заполняем массив
    printf("\nВведи %d чисел:\n", n);
    for (int i = 0; i < n; i++) {
        printf("arr[%d]: ", i);
        scanf("%d", &arr[i]);
    }
    
    // Показываем массив
    printf("\nТвой массив: ");
    for (int i = 0; i < n; i++) {
        printf("%d ", arr[i]);
    }
    printf("\n");
    
    // Вставляем элемент
    printf("\nКуда вставить? (индекс от 0 до %d): ", n);
    scanf("%d", &индекс);
    printf("Какое число вставить? ");
    scanf("%d", &значение);
    
    // Вызываем функцию вставки
    if (вставить_элемент(&arr, &n, индекс, значение)) {
        printf("\nЭлемент вставлен!\n");
        printf("Новый массив: ");
        for (int i = 0; i < n; i++) {
            printf("%d ", arr[i]);
        }
        printf("\n");
        printf("Новый размер массива: %d\n", n);
    } else {
        printf("Не получилось вставить элемент(\n");
    }
    
    // Чистим память
    free(arr);
    
    return 0;
}