#include <stdio.h>
#include <stdlib.h>

// Функция поиска максимального элемента
int найти_максимум(int *arr, int размер) {
    int макс = arr[0];
    for (int i = 1; i < размер; i++) {
        if (arr[i] > макс) {
            макс = arr[i];
        }
    }
    return макс;
}

// Функция поиска минимального элемента
int найти_минимум(int *arr, int размер) {
    int мин = arr[0];
    for (int i = 1; i < размер; i++) {
        if (arr[i] < мин) {
            мин = arr[i];
        }
    }
    return мин;
}

// Функция подсчета суммы элементов
int найти_сумму(int *arr, int размер) {
    int сумма = 0;
    for (int i = 0; i < размер; i++) {
        сумма += arr[i];
    }
    return сумма;
}

int main() {
    int *arr;
    int n;
    
    // Спрашиваем размер
    printf("Сколько чисел будет в массиве? ");
    scanf("%d", &n);
    
    // Проверка на дурака
    if (n <= 0) {
        printf("Размер должен быть положительным!\n");
        return 1;
    }
    
    // Выделяем память
    arr = (int*)malloc(n * sizeof(int));
    if (arr == NULL) {
        printf("Память не выделилась(\n");
        return 1;
    }
    
    // Заполняем массив
    printf("\nВведи %d целых чисел:\n", n);
    for (int i = 0; i < n; i++) {
        printf("Число %d: ", i + 1);
        scanf("%d", &arr[i]);
    }
    
    // Выводим сам массив
    printf("\nТвой массив: ");
    for (int i = 0; i < n; i++) {
        printf("%d ", arr[i]);
    }
    printf("\n");
    
    // Ищем всё, что нужно
    int макс = найти_максимум(arr, n);
    int мин = найти_минимум(arr, n);
    int сумма = найти_сумму(arr, n);
    
    // Выводим результаты красиво
    printf("\n========== РЕЗУЛЬТАТЫ ==========\n");
    printf("Максимальный элемент: %d\n", макс);
    printf("Минимальный элемент:  %d\n", мин);
    printf("Сумма всех элементов: %d\n", сумма);
    printf("Среднее арифметическое: %.2f\n", (float)сумма / n);
    printf("================================\n");
    
    // Освобождаем память
    free(arr);
    
    return 0;
}