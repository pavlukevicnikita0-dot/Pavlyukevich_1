#include <stdio.h>
#include <stdlib.h>

// Функция для удаления элемента по индексу
int удалить_элемент(int **arr, int *размер, int индекс) {
    // Проверяем, корректный ли индекс
    if (индекс < 0 || индекс >= *размер) {
        printf("Ошибка: неверный индекс!\n");
        return 0; // Не удалось
    }
    
    // Сдвигаем все элементы влево начиная с индекса
    for (int i = индекс; i < *размер - 1; i++) {
        (*arr)[i] = (*arr)[i + 1];
    }
    
    // Уменьшаем размер на 1
    (*размер)--;
    
    // Перевыделяем память под новый размер
    int *temp = (int*)realloc(*arr, (*размер) * sizeof(int));
    if (temp == NULL && *размер > 0) {
        printf("Предупреждение: не удалось уменьшить память!\n");
        return 0;
    }
    
    *arr = temp;
    return 1; // Успешно
}

int main() {
    int *arr;
    int n, индекс;
    
    // Создаём массив
    printf("Сколько элементов будет в массиве? ");
    scanf("%d", &n);
    
    if (n <= 0) {
        printf("Размер должен быть положительным!\n");
        return 1;
    }
    
    // Выделяем память
    arr = (int*)malloc(n * sizeof(int));
    if (arr == NULL) {
        printf("Память не выделилась!\n");
        return 1;
    }
    
    // Заполняем массив
    printf("\nВведи %d чисел:\n", n);
    for (int i = 0; i < n; i++) {
        printf("arr[%d]: ", i);
        scanf("%d", &arr[i]);
    }
    
    // Показываем массив
    printf("\nТвой массив: ");
    for (int i = 0; i < n; i++) {
        printf("%d ", arr[i]);
    }
    printf("\n");
    
    // Удаляем элемент
    printf("\nКакой элемент удалить? (индекс от 0 до %d): ", n - 1);
    scanf("%d", &индекс);
    
    // Вызываем функцию удаления
    if (удалить_элемент(&arr, &n, индекс)) {
        printf("\nЭлемент удалён!\n");
        printf("Новый массив: ");
        for (int i = 0; i < n; i++) {
            printf("%d ", arr[i]);
        }
        printf("\n");
        printf("Новый размер массива: %d\n", n);
    } else {
        printf("Не получилось удалить элемент(\n");
    }
    
    // Чистим память
    free(arr);
    
    return 0;
}