#include <stdio.h>

int main() {
    int a, b, c;  // Объявляем три переменные типа int
    
    // Выводим адреса переменных (в шестнадцатеричном формате)
    printf("Адрес a: %p\n", (void*)&a);
    printf("Адрес b: %p\n", (void*)&b);
    printf("Адрес c: %p\n\n", (void*)&c);
    
    // Вычисляем разницу между адресами
    // %td используется для вывода разницы указателей (ptrdiff_t)
    printf("Разница между a и b: %td байт\n", &a - &b);
    printf("Разница между b и c: %td байт\n", &b - &c);
    printf("Разница между a и c: %td байт\n", &a - &c);
    
    return 0;
}